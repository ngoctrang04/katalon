public class readfile {
	@Keyword
	public readfilecsv(String file) {
		List<String> List = new ArrayList()
		FileReader fr = new FileReader(file)
		BufferedReader br = new BufferedReader(fr)
		String line = ''
		while (true) {
			line = br.readLine()
			if (line == null) {
				break
			}
			String[] data = line.split(",")
			List.add(data)
		}
		return List
	}
}
